import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = join(__dirname, "..");
const globalsCssPath = join(projectRoot, "app", "globals.css");
const outputPath = join(
  projectRoot,
  "app",
  "src",
  "lib",
  "brand-theme.defaults.generated.ts",
);

const CSS_VAR_TO_KEY = {
  "--brand-primary": "brand_primary",
  "--brand-primary-2": "brand_primary_2",
  "--brand-primary-3": "brand_primary_3",
  "--brand-secondary": "brand_secondary",
  "--brand-success": "brand_success",
  "--brand-success-2": "brand_success_2",
  "--brand-danger": "brand_danger",
  "--brand-danger-2": "brand_danger_2",
};

function extractRootBlock(css) {
  const rootIndex = css.indexOf(":root");
  if (rootIndex === -1) {
    return "";
  }

  const braceStart = css.indexOf("{", rootIndex);
  if (braceStart === -1) {
    return "";
  }

  let depth = 0;
  for (let index = braceStart; index < css.length; index += 1) {
    const char = css[index];
    if (char === "{") {
      depth += 1;
    } else if (char === "}") {
      depth -= 1;
      if (depth === 0) {
        return css.slice(braceStart + 1, index);
      }
    }
  }

  return "";
}

function parseDefaultBrandThemeFromGlobalsCss(css) {
  const rootBlock = extractRootBlock(css);
  const theme = {};

  for (const [cssVar, key] of Object.entries(CSS_VAR_TO_KEY)) {
    const pattern = new RegExp(
      `${cssVar.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\s*:\\s*(#[0-9A-Fa-f]{6})`,
      "i",
    );
    const match = rootBlock.match(pattern);

    if (!match?.[1]) {
      throw new Error(
        `Missing ${cssVar} hex value in app/globals.css :root block`,
      );
    }

    theme[key] = match[1];
  }

  return theme;
}

const globalsCss = readFileSync(globalsCssPath, "utf8");
const defaults = parseDefaultBrandThemeFromGlobalsCss(globalsCss);

const output = `// This file is auto-generated by scripts/sync-brand-theme-from-css.mjs
// Source of truth: app/globals.css (:root brand variables)
// Do not edit manually — run "pnpm sync:brand-theme" after changing globals.css

export const DEFAULT_BRAND_THEME = ${JSON.stringify(defaults, null, 2)};
`;

writeFileSync(outputPath, output, "utf8");
console.log(`Synced brand theme defaults from globals.css -> ${outputPath}`);
